package com.bizstudio.view.pages.application.components;

import java.io.IOException;
import java.net.URL;
import java.util.Locale;
import java.util.ResourceBundle;
import java.util.logging.Level;
import java.util.logging.Logger;
import javafx.fxml.FXMLLoader;
import javafx.fxml.Initializable;
import javafx.scene.layout.HBox;

/**
 * FXML Controller class
 *
 * @author ObinnaAsuzu
 */
public class ApplicationTopBar extends HBox implements Initializable {

    public ApplicationTopBar() {
        FXMLLoader fxmlLoader = new FXMLLoader(
                getClass().getResource("/fxml/application/ApplicationTopBar.fxml"),
                ResourceBundle.getBundle("bundles.application.lang", Locale.ENGLISH));
        fxmlLoader.setRoot(this);
        fxmlLoader.setController(this);

        try {
            fxmlLoader.load();
        } catch (IOException ex) {
            Logger.getLogger(Alert.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

    /**
     * Initializes the controller class.
     */
    @Override
    public void initialize(URL url, ResourceBundle rb) {
        // TODO
    }    
    
}
